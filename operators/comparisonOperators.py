# Lets learn about Comparison Operators 
#These are the following:
    # "==" To check if values are equal
    # "!=" To check if values are not equal
    # ">" To check if value is greater than other value
    # "<" To check if value is smaller than other value
    # ">=" To check if value is greater or equal to other value
    # "<=" To Check if value is smaller or equal to other value


# "=="

x = 2
y = 2

print(x == 2) # True

# "!="

x = 2
y = 1

print(x != y) # True

# ">"

print(x > y) # True

# "<"

print(x < y) # False

# ">="

print(x >= y) # True

# "<="

print(x <= y) # False

